function loadGradingData(id)
{
     $.ajax({
        type:'POST',
        url: 'grading-request/ajax/load_grading_subjects',        
        data:{
            id: id,
           _token: CSRF_TOKEN,
        },
        success:function(data)
        {
            $('#subjects_content').html(data);    
        }
    });
}




$(document).on('click','.action_user',function(e)
{
    e.preventDefault();
    

 var action = $(this).attr('data-action');




 
 if(action == "schedule_grading")
 {

  
    var id = $(this).attr('data-id');
    var parent = $(this).attr('data-parent');
    var child = $(this).attr('data-child');

    $('#parent_name').text(parent);
    $('#child_name').text(child);

    loadGradingData(id);

    $("#exampleModal").modal('toggle');

 }

});







